const mutations = {
  SET_NEWS(state, news) {
    state.news = news;
  },
  SET_JOBS(state, jobs) {
    state.jobs = jobs;
  },
  SET_ASKS(state, asks) {
    state.asks = asks;
  },
  SET_USER(state, user) {
    state.user = user;
  },
  SET_QUESTION(state, question) {
    state.question = question;
  },
};
export default mutations;
